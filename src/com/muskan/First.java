package com.muskan;

public class First {
	public static void main(String[] args) {
		int age = 18;
		//Condition statements
		if (age >= 18) {
			System.out.println("Eligible for license");
		} else {
			System.out.println("Not Eligible for license");
		}
		//For loop
		for (int i = 1; i<=10; i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		//While loop
		int i = 1;
		while (i<=5) {
			System.out.print(i+" ");
			i++;
		}
	}
}
